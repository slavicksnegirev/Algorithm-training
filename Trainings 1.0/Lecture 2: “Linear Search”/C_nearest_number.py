"""
C. Ближайшее число
Ограничение времени	1 секунда
Ограничение памяти	64Mb
Ввод	стандартный ввод или input.txt
Вывод	стандартный вывод или output.txt
Напишите программу, которая находит в массиве элемент, самый близкий по величине к  данному числу.

Формат ввода
В первой строке задается одно натуральное число N, не превосходящее 1000 – размер массива. Во второй строке
содержатся N чисел – элементы массива (целые числа, не превосходящие по модулю 1000). В третьей строке вводится
одно целое число x, не превосходящее по модулю 1000.

Формат вывода
Вывести значение элемента массива, ближайшее к x. Если таких чисел несколько, выведите любое из них.
"""

# Ввод данных
N = int(input())  # Размер массива
array = list(map(int, input().split()))  # Элементы массива
x = int(input())  # Заданное число

# Инициализируем переменные
closest = array[0]  # Самый близкий элемент по умолчанию - первый элемент массива
min_diff = abs(array[0] - x)  # Минимальная разница по умолчанию - разница между первым элементом и x

# Проходим по всем элементам массива
for num in array:
    diff = abs(num - x)  # Вычисляем разницу между текущим элементом и x
    if diff < min_diff:  # Если текущая разница меньше минимальной
        min_diff = diff  # Обновляем минимальную разницу
        closest = num  # Обновляем самый близкий элемент

# Вывод результата
print(closest)
