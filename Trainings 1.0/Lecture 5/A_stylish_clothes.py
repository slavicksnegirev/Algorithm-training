"""
A. Стильная одежда

Ограничение времени	1 секунда
Ограничение памяти	64Mb
Ввод	стандартный ввод или input.txt
Вывод	стандартный вывод или output.txt
Глеб обожает шоппинг. Как-то раз он загорелся идеей подобрать себе майку и штаны так, чтобы выглядеть в них
максимально стильно. В понимании Глеба стильность одежды тем больше, чем меньше разница в цвете элементов его одежды.
В наличии имеется N (1 ≤ N ≤ 100 000) маек и M (1 ≤ M ≤ 100 000) штанов, про каждый элемент известен его цвет
(целое число от 1 до 10 000 000). Помогите Глебу выбрать одну майку и одни штаны так, чтобы разница в их цвете была
как можно меньше.
Формат ввода

Сначала вводится информация о майках: в первой строке целое число N (1 ≤ N ≤ 100 000) и во второй N целых чисел
от 1 до 10 000 000 — цвета имеющихся в наличии маек. Гарантируется, что номера цветов идут в возрастающем порядке
(в частности, цвета никаких двух маек не совпадают).
Далее в том же формате идёт описание штанов: их количество M (1 ≤ M ≤ 100 000) и в следующей строке M целых чисел
от 1 до 10 000 000 в возрастающем порядке — цвета штанов.
Формат вывода

Выведите пару неотрицательных чисел — цвет майки и цвет штанов, которые следует выбрать Глебу. Если вариантов
выбора несколько, выведите любой из них.
"""


def find_best_match(tshirts, pants):
    i, j = 0, 0
    min_diff = float('inf')
    best_tshirt, best_pant = -1, -1

    # Пока не вышли за пределы массивов
    while i < len(tshirts) and j < len(pants):
        t_color = tshirts[i]
        p_color = pants[j]

        # Считаем разницу между цветами
        diff = abs(t_color - p_color)

        # Если нашли меньшую разницу, запоминаем пару
        if diff < min_diff:
            min_diff = diff
            best_tshirt = t_color
            best_pant = p_color

        # Двигаем указатели в зависимости от значений
        if t_color < p_color:
            i += 1
        else:
            j += 1

    return best_tshirt, best_pant


# Чтение входных данных
N = int(input())
tshirts = list(map(int, input().split()))
M = int(input())
pants = list(map(int, input().split()))

# Поиск лучшей пары
result = find_best_match(tshirts, pants)

# Вывод результата
print(result[0], result[1])
